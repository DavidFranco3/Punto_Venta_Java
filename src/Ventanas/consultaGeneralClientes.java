    public void cargarTabla() {
        modelo.addColumn("Cliente");
        modelo.addColumn("Nombre completo");
        modelo.addColumn("Sexo");

        consultaGeneralClientesTabla.setModel(modelo);

        query = "select * from clientes";

        try {

            bd = new Conexion();
            s = null;
            s = Conexion.conectar().createStatement();
            res = s.executeQuery(query);
            while (res.next()) {
                datos[0] = res.getString("ID_cliente");
                datos[1] = res.getString("Nombre") + " " + res.getString("Apellido_paterno") + " " + res.getString("apellido_materno");
                datos[2] = res.getString("Sexo");
                modelo.addRow(datos);
            }

            consultaGeneralClientesTabla.setModel(modelo);

        } catch (Exception e) {

        }
    }

