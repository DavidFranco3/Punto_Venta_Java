    public void ver(int opc) throws SQLException {
        JasperReport jasperReport;
        JasperPrint jasperPrint;
        String path = "C:\\Users\\EMPEÃ‘OS TINO\\Documents\\NetBeansProjects\\Proyecto_DISI\\src\\Reportes\\reporteProductos.jasper";

        try {
            Conexion c = new Conexion();
            jasperReport = (JasperReport) JRLoader.loadObjectFromFile(path);
            //se procesa el archivo jasper
            jasperPrint = JasperFillManager.fillReport(jasperReport, null, c.conectar());

            JasperViewer viewer = new JasperViewer(jasperPrint, false);
            viewer.setDefaultCloseOperation(DISPOSE_ON_CLOSE);
            //si la opcion elegida fue 1, se invoca a la opcion que lo genera en PDF
            if (opc == 1) {
                JasperExportManager.exportReportToPdfFile(jasperPrint, "C:\\Reportes\\ReporteProductos.pdf");
                JOptionPane.showMessageDialog(null, "SE HA CREADO EL REPORTE EN C://Reportes");
            }
            //si la opcion fue 2, entonces solo se visualiza el reporte
            if (opc == 2) {
                viewer.setVisible(true);//Se visualiza el reporte
            }
        } catch (JRException ex) {
            System.err.println("Error iReport: " + ex.getMessage());
        }
    }

