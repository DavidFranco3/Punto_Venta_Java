    private void modificarClientesBotonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modificarClientesBotonGuardarActionPerformed
        // TODO add your handling code here:
        id = modificarClientesEscribirID.getText().toString();
        nombre = modificarClientesEscribirNombre.getText().toString();
        apellidoPaterno = modificarClientesEscribirApellidoPaterno.getText().toString();
        apellidoMaterno = modificarClientesEscribirApellidoMaterno.getText().toString();
        sexo = modificarClientesEscribirSexo.getSelectedItem().toString();

        if (id.length() > 0) {
            if (nombre.length() > 2) {
                if (apellidoPaterno.length() > 0) {
                    if (apellidoMaterno.length() > 0) {
                        if (sexo.length() < 14) {

                            try {
                                bd = new Conexion();
                                s = null;
                                s = Conexion.conectar().createStatement();

                                queryotro = "select * from clientes where ID_cliente='" + id + "';";
                                if (!bd.resultado(queryotro).next()) {

                                    JOptionPane.showMessageDialog(null, "Este cliente no existe");
                                    modificarClientesEscribirID.setText(null);
                                    modificarClientesEscribirID.requestFocus();
                                    bd.cerrar();
                                } else {
                                    actualizar = "UPDATE clientes SET NOMBRE='" + nombre + "', APELLIDO_PATERNO='" + apellidoPaterno + "', APELLIDO_MATERNO='"
                                            + apellidoMaterno + "', SEXO='" + sexo + "', NOMBRE_COMPLETO ='" + nombre + " " + apellidoPaterno + " " + apellidoMaterno + "' WHERE ID_CLIENTE='" + id + "';";
                                    s.execute(actualizar);

                                    JOptionPane.showMessageDialog(null, "Se ah modificado correctamente");
                                    modificarClientesEscribirID.setText(null);
                                    modificarClientesEscribirNombre.setText(null);
                                    modificarClientesEscribirApellidoPaterno.setText(null);
                                    modificarClientesEscribirApellidoMaterno.setText(null);
                                    modificarClientesEscribirSexo.setSelectedIndex(0);

                                    modificarClientesEscribirID.enable(true);
                                    modificarClientesEscribirNombre.enable(false);
                                    modificarClientesEscribirApellidoPaterno.enable(false);
                                    modificarClientesEscribirApellidoMaterno.enable(false);
                                    modificarClientesEscribirSexo.enable(false);

                                    modificarClientesEscribirID.requestFocus();
                                    modificarClientesBotonGuardar.setEnabled(false);
                                }
                            } catch (SQLException e) {
                                JOptionPane.showMessageDialog(null, "Se ah generado un error," + e);
                            }
                        } else {
                            JOptionPane.showMessageDialog(null, "Por favor rellena los campos correctamente");
                            modificarClientesEscribirSexo.requestFocus();
                        }
                    } else {
                        JOptionPane.showMessageDialog(null, "Por favor rellena los campos correctamente");
                        modificarClientesEscribirApellidoMaterno.requestFocus();
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Por favor rellena los campos correctamente");
                    modificarClientesEscribirApellidoPaterno.requestFocus();
                }
            } else {
                JOptionPane.showMessageDialog(null, "Por favor rellena los campos correctamente");
                modificarClientesEscribirNombre.requestFocus();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Por favor realiza una busqueda");
            modificarClientesEscribirID.requestFocus();
        }

    }//GEN-LAST:event_modificarClientesBotonGuardarActionPerformed

