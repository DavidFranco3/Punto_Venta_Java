    private void modificarClientesBotonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modificarClientesBotonBuscarActionPerformed
        // TODO add your handling code here:
        id = modificarClientesEscribirID.getText().toString();

        if (id.length() == 0) {
            JOptionPane.showMessageDialog(null, "Por favor ingresa un ID");
            modificarClientesEscribirID.requestFocus();
        } else {

            try {
                bd = new Conexion();
                s = null;
                s = Conexion.conectar().createStatement();
                queryotro = "select * from clientes where ID_cliente='" + id + "';";
                res = bd.resultado(queryotro);

                if (res.next()) {

                    modificarClientesEscribirID.enable(false);
                    modificarClientesEscribirNombre.enable(true);
                    modificarClientesEscribirApellidoPaterno.enable(true);
                    modificarClientesEscribirApellidoMaterno.enable(true);
                    modificarClientesEscribirSexo.enable(true);

                    modificarClientesEscribirNombre.setText(res.getString("nombre"));
                    modificarClientesEscribirApellidoPaterno.setText(res.getString("apellido_paterno"));
                    modificarClientesEscribirApellidoMaterno.setText(res.getString("apellido_materno"));
                    modificarClientesEscribirSexo.setSelectedItem(res.getString("sexo"));

                    modificarClientesEscribirNombre.requestFocus();
                    modificarClientesBotonGuardar.setEnabled(true);

                    bd.cerrar();
                } else {

                    JOptionPane.showMessageDialog(null, "el cliente no existe");
                    modificarClientesEscribirID.setText(null);
                    modificarClientesEscribirID.requestFocus();
                }
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Se ah generado un error," + e);
            }
        }
    }//GEN-LAST:event_modificarClientesBotonBuscarActionPerformed

