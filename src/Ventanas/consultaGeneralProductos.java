    public void cargarTabla() {
        modelo.addColumn("Codigo de barras");
        modelo.addColumn("Descripcion");
        modelo.addColumn("Cantidad");
        modelo.addColumn("Precio de compra");
        modelo.addColumn("Precio de venta");

        consultaGeneralProductosTabla.setModel(modelo);

        query = "select * from productos_existencia";

        try {

            bd = new Conexion();
            s = null;
            s = Conexion.conectar().createStatement();
            res = s.executeQuery(query);

            while (res.next()) {
                datos[0] = res.getString("ID_producto");
                datos[1] = res.getString("Nombre") + " " + res.getString("Tipo") + " " + res.getString("Tama√±o");
                datos[2] = res.getString("Existencia");
                datos[3] = "$" + res.getString("Precio_compra");
                datos[4] = "$" + res.getString("Precio_venta");
                modelo.addRow(datos);
                bandera = 1;
            }

            consultaGeneralProductosTabla.setModel(modelo);

        } catch (Exception e) {

        }
    }

