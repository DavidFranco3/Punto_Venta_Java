    public void cargarTabla() {
        modelo.addColumn("Venta");
        modelo.addColumn("Fecha");
        modelo.addColumn("Cliente");
        modelo.addColumn("Total");

        consultaGeneralVentasTabla.setModel(modelo);

        query = "select v.id_venta, v.dia_venta, v.mes_venta, v.año_venta, c.nombre, c.apellido_paterno, c.apellido_materno, v.total from ventas v join clientes c on v.id_cliente=c.id_cliente";

        try {

            bd = new Conexion();
            s = null;
            s = Conexion.conectar().createStatement();
            res = s.executeQuery(query);
            while (res.next()) {
                datos[0] = res.getString("ID_venta");
                datos[1] = res.getString("dia_venta") + "/" + res.getString("mes_venta") + "/" + res.getString("año_venta");
                datos[2] = res.getString("nombre") + " " + res.getString("apellido_paterno") + " " + res.getString("apellido_materno");
                datos[3] = "$" + res.getString("total");
                modelo.addRow(datos);
            }

            consultaGeneralVentasTabla.setModel(modelo);

        } catch (Exception e) {

        }
    }

