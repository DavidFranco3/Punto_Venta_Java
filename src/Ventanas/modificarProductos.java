    private void modificarProductosBotonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modificarProductosBotonGuardarActionPerformed
        // TODO add your handling code here:
        id = modificarProductosEscribirID.getText().toString();
        nombre = modificarProductosEscribirNombre.getText().toString();
        tamaño = modificarProductosEscribirTamaño.getText().toString();
        tipo = modificarProductosEscribirTipo.getText().toString();
        existencia = modificarProductosEscribirCantidadExistencia.getText().toString();
        precioCompra = modificarProductosEscribirPrecioCompra.getText().toString();
        precioVenta = modificarProductosEscribirPrecioVenta.getText().toString();

        if (id.length() > 0) {
            if (nombre.length() > 2) {
                if (tamaño.length() > 0) {
                    if (tipo.length() > 0) {
                        if (existencia.length() > 0) {
                            if (precioCompra.length() > 0) {
                                if (precioVenta.length() > 0) {

                                    try {
                                        bd = new Conexion();
                                        s = null;
                                        s = Conexion.conectar().createStatement();

                                        queryotro = "select * from productos_existencia where ID_producto='" + id + "';";
                                        if (!bd.resultado(queryotro).next()) {

                                            JOptionPane.showMessageDialog(null, "Este producto no existe");
                                            modificarProductosEscribirID.setText(null);
                                            modificarProductosEscribirID.requestFocus();
                                            bd.cerrar();
                                        } else {
                                            actualizar = "UPDATE productos_existencia SET NOMBRE='" + nombre + "', TAMAÑO='" + tamaño + "', TIPO='"
                                                    + tipo + "', EXISTENCIA='" + existencia + "',  PRECIO_COMPRA='" + precioCompra + "', PRECIO_VENTA='" + precioVenta + "', DESCRIPCION='" + nombre + " " + tipo + " " + tamaño +"' WHERE ID_PRODUCTO='" + id + "';";
                                            s.execute(actualizar);

                                            JOptionPane.showMessageDialog(null, "Se ah modificado correctamente");

                                            modificarProductosEscribirID.enable(true);
                                            modificarProductosEscribirNombre.enable(false);
                                            modificarProductosEscribirTamaño.enable(false);
                                            modificarProductosEscribirTipo.enable(false);
                                            modificarProductosEscribirCantidadExistencia.enable(false);
                                            modificarProductosEscribirPrecioCompra.enable(false);
                                            modificarProductosEscribirPrecioVenta.enable(false);

                                            modificarProductosEscribirID.setText(null);
                                            modificarProductosEscribirNombre.setText(null);
                                            modificarProductosEscribirTamaño.setText(null);
                                            modificarProductosEscribirTipo.setText(null);
                                            modificarProductosEscribirCantidadExistencia.setText(null);
                                            modificarProductosEscribirPrecioCompra.setText(null);
                                            modificarProductosEscribirPrecioVenta.setText(null);
                                            modificarProductosEscribirID.requestFocus();

                                            modificarProductosBotonGuardar.setEnabled(false);
                                        }
                                    } catch (SQLException e) {
                                        JOptionPane.showMessageDialog(null, "Se ah generado un error," + e);
                                    }
                                } else {
                                    JOptionPane.showMessageDialog(null, "Por favor rellene los campos correctamente");
                                    modificarProductosEscribirPrecioVenta.requestFocus();
                                }
                            } else {
                                JOptionPane.showMessageDialog(null, "Por favor rellene los campos correctamente");
                                modificarProductosEscribirPrecioCompra.requestFocus();
                            }
                        } else {
                            JOptionPane.showMessageDialog(null, "Por favor rellene los campos correctamente");
                            modificarProductosEscribirCantidadExistencia.requestFocus();
                        }
                    } else {
                        JOptionPane.showMessageDialog(null, "Por favor rellene los campos correctamente");
                        modificarProductosEscribirTipo.requestFocus();
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Por favor rellene los campos correctamente");
                    modificarProductosEscribirTamaño.requestFocus();
                }
            } else {
                JOptionPane.showMessageDialog(null, "Por favor rellene los campos correctamente");
                modificarProductosEscribirNombre.requestFocus();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Por favor realiza una busqueda");
            modificarProductosEscribirID.requestFocus();
        }
    }//GEN-LAST:event_modificarProductosBotonGuardarActionPerformed

