    private void modificarProductosBotonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modificarProductosBotonBuscarActionPerformed
        // TODO add your handling code here:
        id = modificarProductosEscribirID.getText().toString();

        if (id.length() == 0) {
            JOptionPane.showMessageDialog(null, "Por favor ingresa un ID");
            modificarProductosEscribirID.requestFocus();
        } else {
            try {
                bd = new Conexion();
                s = null;
                s = Conexion.conectar().createStatement();
                queryotro = "select * from productos_existencia where ID_producto='" + id + "';";
                res = bd.resultado(queryotro);

                if (res.next()) {

                    modificarProductosEscribirID.enable(false);
                    modificarProductosEscribirNombre.enable(true);
                    modificarProductosEscribirTamaño.enable(true);
                    modificarProductosEscribirTipo.enable(true);
                    modificarProductosEscribirCantidadExistencia.enable(true);
                    modificarProductosEscribirPrecioCompra.enable(true);
                    modificarProductosEscribirPrecioVenta.enable(true);

                    modificarProductosEscribirNombre.setText(res.getString("nombre"));
                    modificarProductosEscribirTamaño.setText(res.getString("tamaño"));
                    modificarProductosEscribirTipo.setText(res.getString("tipo"));
                    modificarProductosEscribirCantidadExistencia.setText(res.getString("existencia"));
                    modificarProductosEscribirPrecioCompra.setText(res.getString("precio_Compra"));
                    modificarProductosEscribirPrecioVenta.setText(res.getString("precio_Venta"));

                    modificarProductosEscribirNombre.requestFocus();
                    modificarProductosBotonGuardar.setEnabled(true);
                    bd.cerrar();
                } else {

                    JOptionPane.showMessageDialog(null, "el producto no existe");
                    modificarProductosEscribirID.setText(null);
                    modificarProductosEscribirID.requestFocus();
                }
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Se ah generado un error," + e);
            }
        }
    }//GEN-LAST:event_modificarProductosBotonBuscarActionPerformed

