    private void modificarProveedoresBotonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modificarProveedoresBotonGuardarActionPerformed
        // TODO add your handling code here:
        id = modificarProveedoresEscribirID.getText().toString();
        empresa = modificarProveedoresEscribirEmpresa.getText().toString();
        telefono = modificarProveedoresEscribirTelefono.getText().toString();
        correo = modificarProveedoresEscribirCorreo.getText().toString();
        calle = modificarProveedoresEscribirCalle.getText().toString();
        numero = modificarProveedoresEscribirNumero.getText().toString();
        colonia = modificarProveedoresEscribirColonia.getText().toString();

        if (id.length() > 0) {
            if (empresa.length() > 0) {
                if (telefono.length() == 10) {
                    if (correo.length() > 0) {
                        if (calle.length() > 0) {
                            if (numero.length() > 0) {
                                if (colonia.length() > 0) {
                
                    try {
                    bd = new Conexion();
                    s = null;
                    s = Conexion.conectar().createStatement();

                    queryotro = "select * from proveedores where ID_proveedor='" + id + "';";
                    if (!bd.resultado(queryotro).next()) {

                        JOptionPane.showMessageDialog(null, "Este proveedor no existe");
                        modificarProveedoresEscribirID.setText(null);
                        modificarProveedoresEscribirID.requestFocus();
                        bd.cerrar();
                    } else {
                        actualizar = "UPDATE proveedores SET EMPRESA='" + empresa + "', TELEFONO='" + telefono + "', CORREO='"
                                + correo + "', CALLE='" + calle + "',  NUMERO='" + numero + "', COLONIA='" + colonia + "' WHERE ID_PROVEEDOR='" + id + "';";
                        s.execute(actualizar);

                        JOptionPane.showMessageDialog(null, "Se ah modificado correctamente");
                        modificarProveedoresEscribirID.setText(null);
                        modificarProveedoresEscribirEmpresa.setText(null);
                        modificarProveedoresEscribirTelefono.setText(null);
                        modificarProveedoresEscribirCorreo.setText(null);
                        modificarProveedoresEscribirCalle.setText(null);
                        modificarProveedoresEscribirNumero.setText(null);
                        modificarProveedoresEscribirColonia.setText(null);

                        modificarProveedoresEscribirID.enable(true);
                        modificarProveedoresEscribirEmpresa.enable(false);
                        modificarProveedoresEscribirTelefono.enable(false);
                        modificarProveedoresEscribirCorreo.enable(false);
                        modificarProveedoresEscribirCalle.enable(false);
                        modificarProveedoresEscribirNumero.enable(false);
                        modificarProveedoresEscribirColonia.enable(false);

                        modificarProveedoresEscribirID.requestFocus();
                        modificarProveedoresBotonGuardar.setEnabled(false);
                    }
                } catch (SQLException e) {
                    JOptionPane.showMessageDialog(null, "Se ah generado un error," + e);
                }
            } else {
                                    JOptionPane.showMessageDialog(null, "Por favor rellene los campos correctamente");
                                    modificarProveedoresEscribirColonia.requestFocus();
                                }
                            } else {
                                JOptionPane.showMessageDialog(null, "Por favor rellene los campos correctamente");
                                modificarProveedoresEscribirNumero.requestFocus();
                            }
                        } else {
                            JOptionPane.showMessageDialog(null, "Por favor rellene los campos correctamente");
                            modificarProveedoresEscribirCalle.requestFocus();
                        }
                    } else {
                        JOptionPane.showMessageDialog(null, "Por favor rellene los campos correctamente");
                        modificarProveedoresEscribirCorreo.requestFocus();
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Por favor rellene los campos correctamente");
                    modificarProveedoresEscribirTelefono.requestFocus();
                }
            } else {
                JOptionPane.showMessageDialog(null, "Por favor rellene los campos correctamente");
                modificarProveedoresEscribirEmpresa.requestFocus();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Por favor realiza una busqueda");
            modificarProveedoresEscribirID.requestFocus();
        }
    }//GEN-LAST:event_modificarProveedoresBotonGuardarActionPerformed

