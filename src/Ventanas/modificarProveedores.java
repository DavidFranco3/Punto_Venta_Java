    private void modificarProveedoresBotonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modificarProveedoresBotonBuscarActionPerformed
        // TODO add your handling code here:
        id = modificarProveedoresEscribirID.getText().toString();

        if (id.length() == 0) {
            JOptionPane.showMessageDialog(null, "Por favor ingresa un ID");
            modificarProveedoresEscribirID.requestFocus();
        } else {

            try {
                bd = new Conexion();
                s = null;
                s = Conexion.conectar().createStatement();
                queryotro = "select * from proveedores where ID_proveedor='" + id + "';";
                res = bd.resultado(queryotro);

                if (res.next()) {

                    modificarProveedoresEscribirID.enable(false);
                    modificarProveedoresEscribirEmpresa.enable(true);
                    modificarProveedoresEscribirTelefono.enable(true);
                    modificarProveedoresEscribirCorreo.enable(true);
                    modificarProveedoresEscribirCalle.enable(true);
                    modificarProveedoresEscribirNumero.enable(true);
                    modificarProveedoresEscribirColonia.enable(true);

                    modificarProveedoresEscribirEmpresa.setText(res.getString("empresa"));
                    modificarProveedoresEscribirTelefono.setText(res.getString("telefono"));
                    modificarProveedoresEscribirCorreo.setText(res.getString("correo"));
                    modificarProveedoresEscribirCalle.setText(res.getString("calle"));
                    modificarProveedoresEscribirNumero.setText(res.getString("numero"));
                    modificarProveedoresEscribirColonia.setText(res.getString("colonia"));

                    modificarProveedoresEscribirEmpresa.requestFocus();
                    modificarProveedoresBotonGuardar.setEnabled(true);
                    bd.cerrar();
                } else {

                    JOptionPane.showMessageDialog(null, "el proveedor no existe");
                    modificarProveedoresEscribirID.setText(null);
                    modificarProveedoresEscribirID.requestFocus();

                }
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Se ah generado un error," + e);
            }
        }
    }//GEN-LAST:event_modificarProveedoresBotonBuscarActionPerformed

