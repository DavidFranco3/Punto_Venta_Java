    private void consultaIndividualVentasCargarClientes(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_consultaIndividualVentasCargarClientes
        // TODO add your handling code here:
        try {
            bd = new Conexion();
            s = null;
            s = Conexion.conectar().createStatement();
            res = s.executeQuery("select * from Clientes");
            while (res.next()) {

                listaClientes.add(res.getString("Nombre_completo"));
            }

            if (bandera == 0) {
                consultaIndividualVentasEscribirCliente.addItem("Elige un cliente");//posicion 0 de la lista del combo
                for (int i = 0; i <= listaClientes.size(); i++) {
                    consultaIndividualVentasEscribirCliente.addItem(listaClientes.get(i));
                    bandera = 1;
                }
            }

        } catch (Exception e) {

        }
    }//GEN-LAST:event_consultaIndividualVentasCargarClientes

