    private void consultaIndividualVentasBotonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_consultaIndividualVentasBotonBuscarActionPerformed
        // TODO add your handling code here:
        id = consultaIndividualVentasEscribirID.getText().toString();

        if (id.length() == 0) {
            JOptionPane.showMessageDialog(null, "Por favor ingresa un ID");
            consultaIndividualVentasEscribirID.requestFocus();
        } else {

            try {
                bd = new Conexion();
                s = null;
                s = Conexion.conectar().createStatement();
                queryotro = "select v.dia_venta, v.mes_venta, v.año_venta, c.nombre_completo, v. total from ventas v join clientes c on v.id_cliente=c.id_cliente where ID_venta='" + id + "';";
                res = bd.resultado(queryotro);

                if (res.next()) {

                    consultaIndividualVentasEscribirDia.setSelectedItem(res.getString("Dia_venta"));
                    consultaIndividualVentasEscribirMes.setSelectedItem(res.getString("Mes_venta"));
                    consultaIndividualVentasEscribirAño.setSelectedItem(res.getString("Año_venta"));
                    consultaIndividualVentasEscribirCliente.setSelectedItem(res.getString("nombre_completo"));
                    consultaIndividualVentasEscribirCostoTotal.setText("$" + res.getString("total"));

                    consultaIndividualVentasEscribirID.requestFocus();
                    bd.cerrar();
                } else {

                    JOptionPane.showMessageDialog(null, "la venta no existe");
                    consultaIndividualVentasEscribirID.setText(null);
                    consultaIndividualVentasEscribirID.requestFocus();
                }
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Se ah generado un error," + e);
            }
        }
    }//GEN-LAST:event_consultaIndividualVentasBotonBuscarActionPerformed

