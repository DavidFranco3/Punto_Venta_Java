    private void eliminarProveedoresBotonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarProveedoresBotonBuscarActionPerformed
        // TODO add your handling code here:
        id = eliminarProveedoresEscribirID.getText().toString();

        if (id.length() == 0) {
            JOptionPane.showMessageDialog(null, "Por favor ingresa un ID");
            eliminarProveedoresEscribirID.requestFocus();
        } else {

            try {
                bd = new Conexion();
                s = null;
                s = Conexion.conectar().createStatement();
                queryotro = "select * from proveedores where ID_proveedor='" + id + "';";
                res = bd.resultado(queryotro);

                if (res.next()) {

                    eliminarProveedoresEscribirID.enable(false);

                    eliminarProveedoresEscribirEmpresa.setText(res.getString("empresa"));
                    eliminarProveedoresEscribirTelefono.setText(res.getString("telefono"));
                    eliminarProveedoresEscribirCorreo.setText(res.getString("correo"));
                    eliminarProveedoresEscribirCalle.setText(res.getString("calle"));
                    eliminarProveedoresEscribirNumero.setText(res.getString("numero"));
                    eliminarProveedoresEscribirColonia.setText(res.getString("colonia"));

                    eliminarProveedoresBotonEliminar.setEnabled(true);

                    bd.cerrar();
                } else {

                    JOptionPane.showMessageDialog(null, "el proveedor no existe");
                    eliminarProveedoresEscribirID.setText(null);
                    eliminarProveedoresEscribirID.requestFocus();
                }
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Se ah generado un error," + e);
            }
        }
    }//GEN-LAST:event_eliminarProveedoresBotonBuscarActionPerformed

