    private void eliminarProveedoresBotonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarProveedoresBotonEliminarActionPerformed
        // TODO add your handling code here:
        id = eliminarProveedoresEscribirID.getText().toString();
        empresa = eliminarProveedoresEscribirEmpresa.getText().toString();
        telefono = eliminarProveedoresEscribirTelefono.getText().toString();
        correo = eliminarProveedoresEscribirCorreo.getText().toString();
        calle = eliminarProveedoresEscribirCalle.getText().toString();
        numero = eliminarProveedoresEscribirNumero.getText().toString();
        colonia = eliminarProveedoresEscribirColonia.getText().toString();

        if (id.length() > 0) {
            if (empresa.length() > 0 && telefono.length() == 10 && correo.length() > 0 && calle.length() > 0 && numero.length() > 0 && colonia.length() > 0) {

                try {
                    bd = new Conexion();
                    s = null;
                    s = Conexion.conectar().createStatement();

                    queryotro = "select * from proveedores where ID_proveedor='" + id + "';";
                    respuesta = JOptionPane.showConfirmDialog(null, "¿Estas seguro que deseas eliminar el registro?", "Confirmar salida", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
                    if (respuesta == JOptionPane.YES_OPTION) {
                        if (!bd.resultado(queryotro).next()) {

                            JOptionPane.showMessageDialog(null, "Este proveedor no existe");
                            eliminarProveedoresEscribirID.setText(null);
                            eliminarProveedoresEscribirID.requestFocus();
                            bd.cerrar();
                        } else {

                            eliminar = "delete from proveedores where ID_PROVEEDOR='" + id + "';";
                            s.execute(eliminar);

                            JOptionPane.showMessageDialog(null, "Se ah eliminado correctamente");
                            eliminarProveedoresEscribirID.setText(null);
                            eliminarProveedoresEscribirEmpresa.setText(null);
                            eliminarProveedoresEscribirTelefono.setText(null);
                            eliminarProveedoresEscribirCorreo.setText(null);
                            eliminarProveedoresEscribirCalle.setText(null);
                            eliminarProveedoresEscribirNumero.setText(null);
                            eliminarProveedoresEscribirColonia.setText(null);

                            eliminarProveedoresEscribirID.enable(true);
                            eliminarProveedoresEscribirID.requestFocus();
                            eliminarProveedoresBotonEliminar.setEnabled(false);
                        }
                    } else if (respuesta == JOptionPane.NO_OPTION) {
                        id = eliminarProveedoresEscribirID.getText().toString();
                        empresa = eliminarProveedoresEscribirEmpresa.getText().toString();
                        telefono = eliminarProveedoresEscribirTelefono.getText().toString();
                        correo = eliminarProveedoresEscribirCorreo.getText().toString();
                        calle = eliminarProveedoresEscribirCalle.getText().toString();
                        numero = eliminarProveedoresEscribirNumero.getText().toString();
                        colonia = eliminarProveedoresEscribirColonia.getText().toString();
                    }
                } catch (SQLException e) {
                    JOptionPane.showMessageDialog(null, "Se ah generado un error," + e);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Es necesario visualizar todos los campos para eliminar");
                eliminarProveedoresEscribirID.requestFocus();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Por favor realiza una busqueda");
            eliminarProveedoresEscribirID.requestFocus();
        }
    }//GEN-LAST:event_eliminarProveedoresBotonEliminarActionPerformed

