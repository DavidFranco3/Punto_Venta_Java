    private void registrarProductosBotonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registrarProductosBotonGuardarActionPerformed
        // TODO add your handling code here:

        id = registrarProductosEscribirID.getText().toString();
        nombre = registrarProductosEscribirNombre.getText().toString();
        tamaño = registrarProductosEscribirTamaño.getText().toString();
        tipo = registrarProductosEscribirTipo.getText().toString();
        existencia = registrarProductosEscribirCantidadExistencia.getText().toString();
        precioCompra = registrarProductosEscribirPrecioCompra.getText().toString();
        precioVenta = registrarProductosEscribirPrecioVenta.getText().toString();

        if (id.length() > 0) {
            if (nombre.length() > 2) {
                if (tamaño.length() > 0) {
                    if (tipo.length() > 0) {
                        if (existencia.length() > 0) {
                            if (precioCompra.length() > 0) {
                                if (precioVenta.length() > 0) {

                                    try {
                                        bd = new Conexion();
                                        s = null;
                                        s = Conexion.conectar().createStatement();

                                        queryotro = "select * from productos_existencia where ID_producto='" + id + "';";
                                        if (bd.resultado(queryotro).next()) {

                                            JOptionPane.showMessageDialog(null, "Ya esta registrado un producto con el mismo ID");
                                            registrarProductosEscribirID.setText("");
                                            registrarProductosEscribirID.requestFocus();
                                            bd.cerrar();
                                        } else {
                                            insertar = "insert into productos_existencia values ('" + id + "', '" + nombre + "', '" + tamaño + "', '" + tipo + "', '" + existencia + "', '" + precioCompra + "', '" + precioVenta + "', '" + nombre + " " + tipo + " " + tamaño +"');";
                                            s.execute(insertar);

                                            JOptionPane.showMessageDialog(null, "Se ah insertado correctamente");
                                            registrarProductosEscribirID.setText(null);
                                            registrarProductosEscribirNombre.setText(null);
                                            registrarProductosEscribirTamaño.setText(null);
                                            registrarProductosEscribirTipo.setText(null);
                                            registrarProductosEscribirCantidadExistencia.setText(null);
                                            registrarProductosEscribirPrecioCompra.setText(null);
                                            registrarProductosEscribirPrecioVenta.setText(null);
                                            registrarProductosEscribirID.requestFocus();
                                        }
                                    } catch (SQLException e) {
                                        JOptionPane.showMessageDialog(null, "Se ah generado un error," + e);
                                    }
                                } else {
                                    JOptionPane.showMessageDialog(null, "Por favor rellene los campos correctamente");
                                    registrarProductosEscribirPrecioVenta.requestFocus();
                                }
                            } else {
                                JOptionPane.showMessageDialog(null, "Por favor rellene los campos correctamente");
                                registrarProductosEscribirPrecioCompra.requestFocus();
                            }
                        } else {
                            JOptionPane.showMessageDialog(null, "Por favor rellene los campos correctamente");
                            registrarProductosEscribirCantidadExistencia.requestFocus();
                        }
                    } else {
                        JOptionPane.showMessageDialog(null, "Por favor rellene los campos correctamente");
                        registrarProductosEscribirTipo.requestFocus();
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Por favor rellene los campos correctamente");
                    registrarProductosEscribirTamaño.requestFocus();
                }
            } else {
                JOptionPane.showMessageDialog(null, "Por favor rellene los campos correctamente");
                registrarProductosEscribirNombre.requestFocus();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Por favor rellene los campos correctamente");
            registrarProductosEscribirID.requestFocus();
        }
    }//GEN-LAST:event_registrarProductosBotonGuardarActionPerformed

