    private void eliminarProductosBotonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarProductosBotonBuscarActionPerformed
        // TODO add your handling code here:
        id = eliminarProductosEscribirID.getText().toString();

        if (id.length() == 0) {
            JOptionPane.showMessageDialog(null, "Por favor ingresa un ID");
            eliminarProductosEscribirID.requestFocus();
        } else {

            try {
                bd = new Conexion();
                s = null;
                s = Conexion.conectar().createStatement();
                queryotro = "select * from productos_existencia where ID_producto='" + id + "';";
                res = bd.resultado(queryotro);

                if (res.next()) {

                    eliminarProductosEscribirID.enable(false);

                    eliminarProductosEscribirNombre.setText(res.getString("nombre"));
                    eliminarProductosEscribirTamaño.setText(res.getString("tamaño"));
                    eliminarProductosEscribirTipo.setText(res.getString("tipo"));
                    eliminarProductosEscribirCantidadExistencia.setText(res.getString("existencia"));
                    eliminarProductosEscribirPrecioCompra.setText(res.getString("precio_Compra"));
                    eliminarProductosEscribirPrecioVenta.setText(res.getString("precio_Venta"));

                    eliminarProductosBotonEliminar.setEnabled(true);

                    bd.cerrar();
                } else {

                    JOptionPane.showMessageDialog(null, "el producto no existe");
                    eliminarProductosEscribirID.setText(null);
                    eliminarProductosEscribirID.requestFocus();
                }
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Se ah generado un error," + e);
            }
        }
    }//GEN-LAST:event_eliminarProductosBotonBuscarActionPerformed

