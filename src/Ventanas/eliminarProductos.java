    private void eliminarProductosBotonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarProductosBotonEliminarActionPerformed
        // TODO add your handling code here:
        id = eliminarProductosEscribirID.getText().toString();
        nombre = eliminarProductosEscribirNombre.getText().toString();
        tamaño = eliminarProductosEscribirTamaño.getText().toString();
        tipo = eliminarProductosEscribirTipo.getText().toString();
        existencia = eliminarProductosEscribirCantidadExistencia.getText().toString();
        precioCompra = eliminarProductosEscribirPrecioCompra.getText().toString();
        precioVenta = eliminarProductosEscribirPrecioVenta.getText().toString();

        if (id.length() > 0) {

            if (nombre.length() > 2 && tamaño.length() > 0 && tipo.length() > 0 && existencia.length() > 0 && precioCompra.length() > 0 && precioVenta.length() > 0) {

                try {
                    bd = new Conexion();
                    s = null;
                    s = Conexion.conectar().createStatement();
                    queryotro = "select * from productos_existencia where ID_producto='" + id + "';";

                    respuesta = JOptionPane.showConfirmDialog(null, "¿Estas seguro que deseas eliminar el registro?", "Confirmar salida", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
                    if (respuesta == JOptionPane.YES_OPTION) {
                        if (!bd.resultado(queryotro).next()) {

                            JOptionPane.showMessageDialog(null, "Este producto no existe");
                            eliminarProductosEscribirID.setText(null);
                            eliminarProductosEscribirID.requestFocus();
                            bd.cerrar();
                        } else {

                            eliminar = "delete from productos_existencia where ID_PRODUCTO='" + id + "';";
                            s.execute(eliminar);
                            JOptionPane.showMessageDialog(null, "Se ah eliminado correctamente");
                            eliminarProductosEscribirID.setText(null);
                            eliminarProductosEscribirNombre.setText(null);
                            eliminarProductosEscribirTamaño.setText(null);
                            eliminarProductosEscribirTipo.setText(null);
                            eliminarProductosEscribirCantidadExistencia.setText(null);
                            eliminarProductosEscribirPrecioCompra.setText(null);
                            eliminarProductosEscribirPrecioVenta.setText(null);

                            eliminarProductosEscribirID.enable(true);
                            eliminarProductosEscribirID.requestFocus();
                            eliminarProductosBotonEliminar.setEnabled(false);
                        }
                    } else if (respuesta == JOptionPane.NO_OPTION) {
                        id = eliminarProductosEscribirID.getText().toString();
                        nombre = eliminarProductosEscribirNombre.getText().toString();
                        tamaño = eliminarProductosEscribirTamaño.getText().toString();
                        tipo = eliminarProductosEscribirTipo.getText().toString();
                        existencia = eliminarProductosEscribirCantidadExistencia.getText().toString();
                        precioCompra = eliminarProductosEscribirPrecioCompra.getText().toString();
                        precioVenta = eliminarProductosEscribirPrecioVenta.getText().toString();
                    }
                } catch (SQLException e) {
                    JOptionPane.showMessageDialog(null, "Se ah generado un error," + e);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Es necesario visualizar todos los campos para eliminar");
                eliminarProductosEscribirID.requestFocus();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Por favor realiza una busqueda");
            eliminarProductosEscribirID.requestFocus();
        }
    }//GEN-LAST:event_eliminarProductosBotonEliminarActionPerformed

